import * as ActionTypes from './ActionTypes';
import { baseUrl } from '../shared/baseUrl';

// Dish Action creators
export const fetchDishes = () => (dispatch) => {

    dispatch(dishesLoading(true));

    return fetch(baseUrl + 'dishes')
    .then(response => {
        if (response.ok) {
          return response; // this response will be available to the next .then()
        } else {
          var error = new Error('Error ' + response.status + ': ' + response.statusText);
          error.response = response;
          throw error; // will be caught in the catch block
        }
      },
      error => {
            var errMess = new Error(error.message);
            throw errMess; // will be caught in the catch block
      })
    .then(response => response.json()) // when promise resolves, convert response tp JSON so that can be processed after
    .then(dishes => dispatch(addDishes(dishes))) // add dishes to the state
    .catch(error => dispatch(dishesFailed(error.message)));
}

// Dish Actions
export const dishesLoading = () => ({
    type: ActionTypes.DISHES_LOADING
});

export const dishesFailed = (errmess) => ({
    type: ActionTypes.DISHES_FAILED,
    payload: errmess
});

export const addDishes = (dishes) => ({
    type: ActionTypes.ADD_DISHES,
    payload: dishes
});


// Comments Action creators
// Action creator to post a comment
export const postComment = (dishId, rating, author, comment) => (dispatch) => {

    const newComment = {
        dishId: dishId,
        rating: rating,
        author: author,
        comment: comment
    };
    newComment.date = new Date().toISOString();
    
    return fetch(baseUrl + 'comments', {
        method: "POST",
        body: JSON.stringify(newComment),
        headers: {
          "Content-Type": "application/json"
        },
        credentials: "same-origin"
    })
    .then(response => {
        if (response.ok) {
          return response;
        } else {
          var error = new Error('Error ' + response.status + ': ' + response.statusText);
          error.response = response;
          throw error;
        }
      },
      error => {
            throw error;
      })
    .then(response => response.json())
    .then(response => dispatch(addComment(response)))
    .catch(error =>  {
        console.log('post comments', error.message);
        alert('Your comment could not be posted\nError: '+error.message);
    });
};

